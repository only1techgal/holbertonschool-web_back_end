"""
Module 3-to_str.py
This module contains a single function, `to_str`,
which calculates the sum of two floating-point numbers.
"""


def to_str(n: float) -> str:
    """
    Converts a float to its string representation.

    Parameters:
    n(float): The float to convert.

    Returns:
    str: The string representation of the float.
    """
    return str(n)
